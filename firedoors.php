        <ul class="content-list">
            <li class="content-item">
                <strong class="content-emphasis">Fire Resistance:</strong> Fire doors are constructed to withstand fire and heat for a specified period, typically 30 minutes, 60 minutes, 90 minutes, or 120 minutes. The fire resistance rating depends on the type of building and its intended use.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Material:</strong> Fire doors are often made from materials with excellent fire-resistant properties, such as solid timber, steel, or fire-resistant glass. The door's core may contain materials like fire-resistant panels.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Seals:</strong> Fire doors are equipped with intumescent seals around the door edges. These seals expand when exposed to heat, creating a barrier that prevents smoke and flames from passing through gaps around the door.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Closers and Latching Devices:</strong> Fire doors must be self-closing and equipped with latching devices to ensure they remain closed in the event of a fire. Some may also have electromagnetic hold-open devices that release when the fire alarm is triggered.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Labeling and Certification:</strong> Fire doors are typically labeled and certified by regulatory authorities or testing agencies to verify their fire-resistant properties. Common certification standards include UL (Underwriters Laboratories) or BS (British Standards).
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Frames and Hardware:</strong> The door frames and associated hardware, including hinges, locks, and handles, must also be fire-rated and compatible with the fire door.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Fire-Resistant Glazing:</strong> In some cases, fire doors have fire-resistant glazing to allow visibility and light transmission while maintaining fire resistance. Fire-resistant glass, such as wired or laminated glass, is used for this purpose.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Location:</strong> Fire doors are strategically placed within a building to create fire compartments, which limit the fire's spread. They are often found in corridors, stairwells, and fire exits.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Regulatory Compliance:</strong> Fire doors are subject to local building codes and regulations. Compliance with these codes is crucial to ensuring the safety and effectiveness of fire doors.
            </li>
            <li class="content-item">
                <strong class="content-emphasis">Regular Maintenance:</strong> It is essential to maintain and inspect fire doors regularly to ensure they function correctly. Damaged or malfunctioning fire doors should be repaired or replaced promptly.
            </li>
        </ul>
